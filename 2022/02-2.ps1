function Show-Object() {
    [CmdletBinding()]
    param (
        [Parameter(ValueFromPipeline=$true)][Object]$InputObject,
        [Parameter()][String]$Prefix="[Show-Object]"
    )
    begin {}
    process {
        Write-Host -NoNewline -ForegroundColor Yellow "$Prefix "
        Write-Host $InputObject
        $InputObject
    }
    end {}
}

function Invoke-Part1($textinput) {

    $rpsOpponent = @{
        "A"="R"
        "B"="P"
        "C"="S"
    }

    $rpsYou = @{
        "X"="R"
        "Y"="P"
        "Z"="S"
    }

    $rpsScores = @{
        "R" = 1
        "P" = 2
        "S" = 3
    }

    $winScores = @{
        "W" = 6
        "L" = 0
        "D" = 3
    }

    $endTable = @{
        "X"="L"
        "Y"="D"
        "Z"="W"
    }

    $pTable = @{
        "RW"="P"
        "RL"="S"
        "RD"="R"
        "PW"="S"
        "PL"="R"
        "PD"="P"
        "SW"="R"
        "SL"="P"
        "SD"="S"
    }

    function Get-RPSResult($a, $b) {
        if ($a -eq $b) {"D"} else {
            switch ($a) {
                "R" {if($b -eq "P"){"L"}else{"W"};break}
                "P" {if($b -eq "S"){"L"}else{"W"};break}
                "S" {if($b -eq "R"){"L"}else{"W"};break}
            }
        }
    }

    $textinput.Split("`n", [System.StringSplitOptions]::None)
    | Show-Object
    | % -Begin {$i=0} {
        $strat = $_.Split(" ")
        $play = $pTable["$($rpsOpponent[$strat[0]])$($endTable[$strat[1]])"]
        #$result = Get-RPSResult -a $rpsYou[$strat[1]] -b $rpsOpponent[$strat[0]]
        [PSCustomObject]@{
            Index = $i
            Opp = $rpsOpponent[$strat[0]]
            You = $play
            Result = $endTable[$strat[1]]
            Score = $winScores[$endTable[$strat[1]]] + $rpsScores[$play]
        }
        $i++
    }
    | Show-Object
    | Measure-Object -Property Score -Sum
}

Invoke-Part1 -textinput $textinput